#classical implementation
def classical_priority_mux(s3, s2, s1, s0):

    if s3 == 1:
        return "Fire Detected (High Priority)"
    elif s2 == 1:
        return "Gas Leakage"
    elif s1 == 1:
        return "Intrusion"
    elif s0 == 1:
        return "Medical Emergency (Low Priority)"
    else:
        return "No Signal"


print(f"Classical Output: {classical_priority_mux(1, 0, 1, 0)}")

