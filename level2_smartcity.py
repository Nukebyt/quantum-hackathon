backend = Aer.get_backend('qasm_simulator')

def run_case(s0, s1, s2, s3):
    qc = QuantumCircuit(5,1)


    if s0: qc.x(0)
    if s1: qc.x(1)
    if s2: qc.x(2)
    if s3: qc.x(3)


    qc.cx(3, 4)

    qc.x(3)
    qc.ccx(3, 2, 4)
    qc.x(3)

    qc.x(3)
    qc.x(2)
    qc.mcx([3,2,1], 4)
    qc.x(2)
    qc.x(3)

    qc.x([3,2,1])
    qc.mcx([3,2,1,0], 4)
    qc.x([3,2,1])

    qc.measure(4,0)

    result = backend.run(qc, shots=1).result()
    return list(result.get_counts().keys())[0]

